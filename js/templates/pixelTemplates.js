import { PixelMatrix } from "../models/PixelMatrix.js";

function createTemplateMatrix(pixels, size = 20) {
  const matrix = PixelMatrix.createZeroMatrix(size);

  pixels.forEach(([rowIndex, columnIndex, value]) => {
    if (rowIndex >= 0 && rowIndex < size && columnIndex >= 0 && columnIndex < size) {
      matrix[rowIndex][columnIndex] = value;
    }
  });

  return matrix;
}

